function RangeSliderHandler(SliderId, unitIndex) {
    var
        element = $('#' + SliderId),
        value = element.val(),
        values = [1, 2, 5]
    let unitNmbr = 0;
    if (value >= 3) {
        unitNmbr = Math.floor(value / 3);
        value = value % 3;
    }
    $('#' + SliderId + 'Value').text("Value: " + values[value] + unit[unitIndex][unitNmbr]);
    return parseInt(values[value]) * 100 * (10 ** unitNmbr);
}

